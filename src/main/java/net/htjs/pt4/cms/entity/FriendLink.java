package net.htjs.pt4.cms.entity;

import java.io.Serializable;

/**
 * 友情链接实体类
 * 
 * @author xieshiyu
 *
 */
public class FriendLink implements Serializable {

	private static final long serialVersionUID = -212901730662097509L;
	// 友情链接ID
	private String friendLinkId;
	// 站点ID
	private String siteId;
	// 友情链接类别ID
	private String typeId;
	// 网站名称
	private String name;
	// 网站地址
	private String domain;
	// 站长邮箱
	private String email;
	// 网站LOGO地址
	private String logoPath;
	// 网站简介
	private String description;
	// 排列顺序
	private Integer priority;
	// 点击次数
	private Integer views;
	// 是否显示，1是，0否
	private boolean display;

	public String getFriendLinkId() {
		return friendLinkId;
	}

	public void setFriendLinkId(String friendLinkId) {
		this.friendLinkId = friendLinkId;
	}

	public String getSiteId() {
		return siteId;
	}

	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}

	public String getTypeId() {
		return typeId;
	}

	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getLogoPath() {
		return logoPath;
	}

	public void setLogoPath(String logoPath) {
		this.logoPath = logoPath;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getPriority() {
		return priority;
	}

	public void setPriority(Integer priority) {
		this.priority = priority;
	}

	public Integer getViews() {
		return views;
	}

	public void setViews(Integer views) {
		this.views = views;
	}

	public boolean isDisplay() {
		return display;
	}

	public void setDisplay(boolean display) {
		this.display = display;
	}

}
