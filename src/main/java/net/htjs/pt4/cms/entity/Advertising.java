package net.htjs.pt4.cms.entity;

import java.io.Serializable;

/**
 * 广告实体类，对应表hj_advertising
 * 
 * @author caojian
 *
 */
public class Advertising implements Serializable {

	private static final long serialVersionUID = 1L;
	// 广告ID
	private String advertising_id;
	// 站点ID
	private String site_id;
	// 名称
	private String name;
	// 广告版位，0页头banner，1通栏
	private Integer category;
	// 广告类别，0图片广告，1文字广告
	private Integer adspace_id;
	// 广告图片地址
	private String ad_img_url;
	// 广告图片宽
	private Integer ad_img_width;
	// 广告图片高
	private Integer ad_img_height;
	// 广告链接地址/广告文字链接
	private String ad_url;
	// 广告链接提示/广告文字内容
	private String ad_info;
	// 广告打开目标，0新窗口，1原窗口
	private Integer ad_blank;
	// 广告文字字体颜色
	private String ad_color;
	// 广告文字字体大小
	private String ad_font;
	// 广告点击次数
	private Integer click_count;
	// 广告权重，值越小越靠前
	private Integer ad_weight;
	// 是否启用，1是，0否
	private Integer is_enabled;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAdvertising_id() {
		return advertising_id;
	}

	public void setAdvertising_id(String advertising_id) {
		this.advertising_id = advertising_id;
	}

	public String getSite_id() {
		return site_id;
	}

	public void setSite_id(String site_id) {
		this.site_id = site_id;
	}

	public Integer getCategory() {
		return category;
	}

	public void setCategory(Integer category) {
		this.category = category;
	}

	public Integer getAdspace_id() {
		return adspace_id;
	}

	public void setAdspace_id(Integer adspace_id) {
		this.adspace_id = adspace_id;
	}

	public String getAd_img_url() {
		return ad_img_url;
	}

	public void setAd_img_url(String ad_img_url) {
		this.ad_img_url = ad_img_url;
	}

	public Integer getAd_img_width() {
		return ad_img_width;
	}

	public void setAd_img_width(Integer ad_img_width) {
		this.ad_img_width = ad_img_width;
	}

	public Integer getAd_img_height() {
		return ad_img_height;
	}

	public void setAd_img_height(Integer ad_img_height) {
		this.ad_img_height = ad_img_height;
	}

	public String getAd_url() {
		return ad_url;
	}

	public void setAd_url(String ad_url) {
		this.ad_url = ad_url;
	}

	public String getAd_info() {
		return ad_info;
	}

	public void setAd_info(String ad_info) {
		this.ad_info = ad_info;
	}

	public Integer getAd_blank() {
		return ad_blank;
	}

	public void setAd_blank(Integer ad_blank) {
		this.ad_blank = ad_blank;
	}

	public String getAd_color() {
		return ad_color;
	}

	public void setAd_color(String ad_color) {
		this.ad_color = ad_color;
	}

	public String getAd_font() {
		return ad_font;
	}

	public void setAd_font(String ad_font) {
		this.ad_font = ad_font;
	}

	public Integer getClick_count() {
		return click_count;
	}

	public void setClick_count(Integer click_count) {
		this.click_count = click_count;
	}

	public Integer getAd_weight() {
		return ad_weight;
	}

	public void setAd_weight(Integer ad_weight) {
		this.ad_weight = ad_weight;
	}

	public Integer getIs_enabled() {
		return is_enabled;
	}

	public void setIs_enabled(Integer is_enabled) {
		this.is_enabled = is_enabled;
	}

}
